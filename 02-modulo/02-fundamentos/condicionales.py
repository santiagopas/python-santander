# condicionales en python

# las condiciones en python se usan para tomar decisiones, existen varios tipos de condicionales: if, elif, else

a = 2000
b = 200

if a > b:
    print(a, "es mayor que", b)
elif a == b:
    print(a, "y", b, "son iguales")
else:
    print(b, "es mayor que", a)


